const height = 200;
const margin = { top: 20, right: 20, bottom: 30, left: 40 };

// Stvori D3.js skalu za osi
const xScale = d3
  .scaleBand()
  .domain(Object.keys(playerWins))
  .range([margin.left, width - margin.right])
  .padding(0.1);

const yScale = d3
  .scaleLinear()
  .domain([0, d3.max(Object.values(playerWins))])
  .range([height - margin.bottom, margin.top]);

// Stvori SVG element za crtanje grafa
const svg_barchart = d3
  .select("#barchart")
  .append("svg")
  .attr("width", width)
  .attr("height", height)
 ;

// Crtaj trake barcharta
svg_barchart
  .selectAll("rect")
  .data(Object.entries(playerWins))
  .enter()
  .append("rect")
  .attr("x", d => xScale(d[0]))
  .attr("y", d => yScale(d[1]))
  .attr("width", xScale.bandwidth())
  .attr("height", d => height - margin.bottom - yScale(d[1]))
  .attr("fill", "green")
  .on("mouseover", (event, d) => {
    // Prikazi vrijednost stupca u prozorÄiÄ‡u
    svg_barchart
      .append("text")
      .attr("x", xScale(d[0]) + xScale.bandwidth() / 2)
      .attr("y", yScale(d[1]) - 5)
      .attr("text-anchor", "middle")
      .text(d[1])
      .attr("class", "value-tooltip");
  })
  .on("mouseout", () => {
    // Ukloni prozorÄiÄ‡ kada miÅ¡ napusti stupac
    svg_barchart.selectAll(".value-tooltip").remove();
  });

// Dodaj osi
svg_barchart
  .append("g")
  .attr("transform", `translate(0,${height - margin.bottom})`)
  .call(d3.axisBottom(xScale));

svg_barchart
  .append("g")
  .attr("transform", `translate(${margin.left},0)`)
  .call(d3.axisLeft(yScale));


